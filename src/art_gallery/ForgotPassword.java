    public void clear(){                            // clear btn er jonno 
        
        B_update.setEnabled(false);
        B_search.setEnabled(false);
        T_email.setEditable(true);
        T_email.setText("");
        T_security.setText("");
        T_answer.setText("");
        T_newpassword.setText("");
        
    }
    
    public void validateEmail(){
        email= T_email.getText();
        if(email.matches(emailPatern)){
            B_search.setEnabled(true);
        }
        else
            B_search.setEnabled(false);
    }
    
    
    public void validateFields(){
        String password = T_newpassword.getText();
        String answer = T_answer.getText();
        String securityQuestion = T_security.getText();
        if(!password.equals("") && !answer.equals("") && !securityQuestion.equals(""))
            B_update.setEnabled(true);
        else
            B_update.setEnabled(false);
    }

