    private void B_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_updateActionPerformed
        // TODO add your handling code here:
        String answer = T_answer.getText();
        String newPassword = T_newpassword.getText();
        if(answer.equals(dbAnswer)){
            UserDao.update(email, newPassword);
            clear();
        }
        else{
            JOptionPane.showMessageDialog(null," Incorrect Email ","Message",JOptionPane.ERROR_MESSAGE);
        }
            
    }//GEN-LAST:event_B_updateActionPerformed

    private void B_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_searchActionPerformed
        // TODO add your handling code here:
        email= T_email.getText();
        User user = null;
        user = UserDao.getSEcurityQuestion(email);
        if(user == null){
            JOptionPane.showMessageDialog(null," Incorrect Email ","Message",JOptionPane.ERROR_MESSAGE);
            
        }
        else                          //if user in not exist
        {
            B_search.setEnabled(false);
            T_email.setEditable(false );
            dbAnswer = user.getAnswer();
            T_security.setText(user.getSecurityQuestion());
            validateFields();
            
        }
    }//GEN-LAST:event_B_searchActionPerformed

